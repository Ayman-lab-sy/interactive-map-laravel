<div class="subject">
    Subject: Submission of information concerning alleged violations affecting persons belonging to minorities
</div>

<div class="paragraph">
    The present submission is respectfully transmitted for the attention of the
    United Nations Special Rapporteur on minority issues, in accordance with the
    mandate established by the Human Rights Council under its Special Procedures.
</div>

<div class="paragraph">
    This communication concerns allegations of acts or measures reportedly affecting
    persons belonging to ethnic, religious, linguistic, or other minorities, as
    described by the source. The information is submitted for the purposes of
    international human rights monitoring.
</div>

<div class="paragraph">
    The accompanying report provides a factual account of the alleged facts, information
    concerning the individual(s) or community affected, the minority or religious
    identity concerned, details regarding the alleged perpetrators as reported, relevant
    contextual information where applicable, and information concerning steps taken or
    remedies pursued at the domestic level.
</div>

<div class="paragraph">
    The information contained in the report is presented strictly as reported, without
    legal qualification, assessment, or attribution of responsibility, and in
    accordance with the established working methods of the United Nations Special
    Procedures.
</div>

<div class="paragraph">
    Identifying details of the affected individual(s) or community and of the reporting
    source have been withheld or anonymized where necessary in order to ensure safety
    and protection. Supporting documentation may be made available upon request,
    subject to consent and protection considerations.
</div>

<div class="paragraph">
    It is respectfully requested that this information be treated confidentially and
    that any action undertaken by the Special Rapporteur take into account the
    protection needs of the individual(s) concerned and any relevant witnesses.
</div>

<div class="closing">
    <p>Please accept the assurances of our highest consideration.</p>
</div>

