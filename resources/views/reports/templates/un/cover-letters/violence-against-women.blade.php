    Council under its Special Procedures.
</div>

<div class="paragraph">
    This communication concerns allegations of violence reportedly committed against
    a woman or girl, occurring in the public and/or private sphere, and falling within
    the scope of the mandate on violence against women and girls, as reported by the
    source. The information is submitted for the purposes of international human rights
    monitoring.
</div>

<div class="paragraph">
    The accompanying report provides a factual account of the alleged acts of violence,
    information concerning the woman or girl affected, details regarding the alleged
    perpetrators as reported, relevant contextual or pattern-related information where
    applicable, and information concerning steps taken or remedies pursued at the
    domestic level, in line with the Special Rapporteur’s working methods.
</div>

<div class="paragraph">
    The information contained in the report is presented strictly as reported, without
    legal qualification, assessment, or attribution of responsibility, and in
    accordance with the established working methods of the United Nations Special
    Procedures.
</div>

<div class="paragraph">
    Identifying details of the woman or girl concerned and of the reporting source have
    been withheld or anonymized where necessary in order to ensure safety and protection.
    Supporting documentation may be made available upon request, subject to consent and
    protection considerations.
</div>

<div class="paragraph">
