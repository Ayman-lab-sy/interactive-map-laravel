<div class="subject">
    Subject: Submission of information concerning alleged enforced or involuntary disappearance
</div>

<div class="paragraph">
    The present submission is respectfully transmitted for the attention of the
    United Nations Working Group on Enforced or Involuntary Disappearances,
    in accordance with its mandate under the Special Procedures of the Human Rights Council.
</div>

<div class="paragraph">
    This communication concerns allegations of enforced or involuntary disappearance,
    involving the deprivation of liberty and the concealment of the fate or whereabouts
    of the affected individual(s), as reported by the source.
</div>

<div class="paragraph">
    The accompanying report provides a factual summary of the alleged circumstances
    of the disappearance, available information regarding the victim(s),
    the alleged perpetrators as described by the source,
    and information concerning steps taken and the availability or exhaustion
    of domestic remedies, in line with the Working Group’s methods of work.
</div>

<div class="paragraph">
    Identifying details of the victim(s) and the reporting source have been withheld
    or anonymized in order to ensure their safety and protection.
    Supporting documentation is available and may be provided upon request,
    subject to consent and protection considerations.
</div>

<div class="paragraph">
    It is respectfully requested that this information be treated confidentially
    and that any action undertaken by the Working Group take into account
    the protection needs of the individuals concerned.
</div>

<div class="closing">
    <p>Please accept the assurances of our highest consideration.</p>
