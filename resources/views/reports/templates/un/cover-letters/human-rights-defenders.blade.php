<div class="subject">
    Subject: Submission of information concerning alleged acts affecting a human rights defender
</div>

<div class="paragraph">
    The present submission is respectfully transmitted for the attention of the
    United Nations Special Rapporteur on the situation of human rights defenders,
    in accordance with the mandate established by the Human Rights Council under
    its Special Procedures.
</div>

<div class="paragraph">
    This communication concerns allegations of acts, measures, or reprisals
    reportedly directed against a human rights defender in connection with their
    legitimate activities in the promotion and protection of human rights, as
    reported by the source, and is submitted for the purposes of international
    human rights monitoring.
</div>

<div class="paragraph">
    The accompanying report provides a factual account of the individual concerned,
    their role as a human rights defender, the activities reportedly carried out,
    the alleged acts or measures taken in response, relevant contextual information
    where applicable, and information regarding steps taken or remedies pursued at
    the domestic level, in line with the Special Rapporteur’s working methods.
</div>

<div class="paragraph">
    The information contained in the report is presented strictly as reported,
    without legal qualification or attribution of responsibility, and in
    accordance with the established working methods of the United Nations
    Special Procedures.
</div>

<div class="paragraph">
    Identifying details of the individual(s) concerned and the reporting source
    have been withheld or anonymized where necessary in order to ensure their
    safety and protection. Supporting documentation may be made available upon
    request, subject to consent and protection considerations.
</div>

<div class="paragraph">
    It is respectfully requested that this information be treated confidentially
    and that any action undertaken by the Special Rapporteur take into account
    the protection needs of the human rights defender(s) concerned.
</div>

<div class="closing">
    <p>Please accept the assurances of our highest consideration.</p>
</div>

